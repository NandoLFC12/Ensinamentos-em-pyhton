# Função para gravar o áudio
def gravar_audio(duracao, taxa_amostragem=44100, nome_arquivo="gravacao.wav"):
    print("Gravando...")

    # Grava o áudio usando o microfone
    grava = sd.rec(int(duracao * taxa_amostragem), samplerate=taxa_amostragem, channels=1, dtype='float32')
    sd.wait()  # Espera até a gravação terminar

    # Salva o áudio em um arquivo WAV
    sf.write(nome_arquivo, grava, taxa_amostragem)
    print(f"Áudio gravado e salvo como {nome_arquivo}")

# Função para reproduzir o áudio gravado
def reproduzir_audio(nome_arquivo="gravacao.wav"):
    # Lê o arquivo de áudio gravado
    dados, taxa_amostragem = sf.read(nome_arquivo)

    # Reproduz o áudio
    print(f"Reproduzindo {nome_arquivo}...")
    sd.play(dados, taxa_amostragem)
    sd.wait()  # Espera até o áudio terminar de tocar

