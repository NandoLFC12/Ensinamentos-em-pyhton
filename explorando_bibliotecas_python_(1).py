"""# 8. Pygame - Desenhando formas geométricas

##  Você está desenvolvendo um aplicativo educativo para ensinar geometria básica para crianças. Implemente uma interface onde formas geométricas como retângulos, círculos e triângulos são desenhados e identificados com etiquetas.

### Bibliotecas/Métodos: pygame.draw.rect(), pygame.draw.circle(), pygame.draw.polygon()
"""

import pygame
import sys

# Inicializando o Pygame
pygame.init()

# Definindo as dimensões da janela
largura, altura = 600, 400
tela = pygame.display.set_mode((largura, altura))
pygame.display.set_caption("Formas Geométricas")

# Definindo cores
BRANCO = (255, 255, 255)
VERDE = (0, 255, 0)
AZUL = (0, 0, 255)
VERMELHO = (255, 0, 0)

# Fonte para as etiquetas
fonte = pygame.font.Font(None, 36)

# Função para desenhar e identificar formas geométricas
def desenhar_formas():
    # Preenche a tela com cor branca
    tela.fill(BRANCO)

    # Desenhando um retângulo
    pygame.draw.rect(tela, VERDE, (50, 50, 150, 100))
    texto_retangulo = fonte.render("Retângulo", True, VERDE)
    tela.blit(texto_retangulo, (50, 50))

    # Desenhando um círculo
    pygame.draw.circle(tela, AZUL, (300, 150), 50)
    texto_circulo = fonte.render("Círculo", True, AZUL)
    tela.blit(texto_circulo, (250, 150))

    # Desenhando um triângulo
    pontos = [(500, 100), (450, 200), (550, 200)]
    pygame.draw.polygon(tela, VERMELHO, pontos)
    texto_triangulo = fonte.render("Triângulo", True, VERMELHO)
    tela.blit(texto_triangulo, (460, 210))

    # Atualiza a tela
    pygame.display.flip()

# Função principal do jogo
def main():
    # Loop principal do jogo
    while True:
        # Captura eventos (como fechar a janela)
        for evento in pygame.event.get():
            if evento.type == pygame.QUIT:
                pygame.quit()
                sys.exit()

        # Chama a função para desenhar as formas
        desenhar_formas()

        # Definir a taxa de atualização
        pygame.time.Clock().tick(60)

# Executa o jogo
main()

